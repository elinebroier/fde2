        <h1 style="font-family: Book Antiqua; font-size: 40px;">Theory</h1>
        Motivation is an incentive that prompts people to take action.
        <br>
        According to Dan Pink (Tedtalk, 2009) there
        are two different types of motivation: extrinsic motivation and intrinsic motivation.
        <br>
        <h4 tyle="font-family: Book Antiqua;">Extrinsic motivation</h4>
        Extrinsic motivation is motivation that comes from the outside.
        Extrinsic motivation comes from two types of external stimuli: reward or punishment.
        So when a person is motivated by external stimuli,
        someone does something for someone else because there is something in return.
        In this case you are not engaging in an act because you enjoy doing it,
        but because you get something in return.
        <br>
        <h4 style="font-family: Book Antiqua;">Intrinsic motivation</h4>
        Intrinsic motivation is the opposite of extrinsic motivation.
        According to Schunk (Schunk, 2010) we speak of intrinsic motivation when the goal of an
        activity lies in the activity itslef. So the motivation comes from whitin. According to Kapp (Kapp, 2012),
        intrinsic motivation has the most positive effect on people's overall motivation.
        Intrinsic motivation has a positive influence. For example: delivering faster solutions, intrinsically
        motivated people come up with a solution to a problem faster than extrinsically motivated people. Also intrinsically motivated
        people think more creatively. Dan Pink (Tedtalk, 2009) concludes
        that intrinsic motivation is the result of three factors:
        <br>
        <ul>
            <li>Someone is intrinsically motivated when someone carries responsibilities and freedoms for his job (autonomy).</li>
            <li>If someone is good at his job and knows it (mastery).</li>
            <li>When someone knows what his work is good for (efficiency).</li>
        </ul>
        <br>
        <br>

